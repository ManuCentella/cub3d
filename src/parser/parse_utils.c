#include "cub3d.h"

void	free_split(char **arr)
{
	int	i;

	i = 0;
	if (!arr)
		return ;
	while (arr[i])
		free(arr[i++]);
	free(arr);
}

int	is_config_complete(t_config *cfg)
{
	return (cfg->no_path && cfg->so_path && cfg->we_path && cfg->ea_path
		&& cfg->floor_color != -1 && cfg->ceiling_color != -1);
}

void	free_config(t_config *cfg)
{
	free(cfg->no_path);
	free(cfg->so_path);
	free(cfg->we_path);
	free(cfg->ea_path);
	// No hace falta liberar colores, son int
}
void	free_map(char **map)
{
	int	i;

	i = 0;
	if (!map)
		return ;
	while (map[i])
		free(map[i++]);
	free(map);
}
